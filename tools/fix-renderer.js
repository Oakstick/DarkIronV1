// This script generates the renderer with grid+axis gizmo
// It avoids template literal issues by using array.join
const fs = require('fs');
const L = [];
function w(s) { L.push(s); }

w('export interface MeshData { name: string; vertices: number[]; indices: number[]; }');
w('export interface RendererConfig { canvas: HTMLCanvasElement; }');
w('');
w('function lookAt(eye:number[],target:number[],up:number[]):Float32Array{');
w('  const zx=eye[0]-target[0],zy=eye[1]-target[1],zz=eye[2]-target[2];');
w('  const zl=Math.sqrt(zx*zx+zy*zy+zz*zz)||1;const z=[zx/zl,zy/zl,zz/zl];');
w('  const xx=up[1]*z[2]-up[2]*z[1],xy=up[2]*z[0]-up[0]*z[2],xz=up[0]*z[1]-up[1]*z[0];');
w('  const xl=Math.sqrt(xx*xx+xy*xy+xz*xz)||1;const x=[xx/xl,xy/xl,xz/xl];');
w('  const y=[z[1]*x[2]-z[2]*x[1],z[2]*x[0]-z[0]*x[2],z[0]*x[1]-z[1]*x[0]];');
w('  return new Float32Array([x[0],y[0],z[0],0,x[1],y[1],z[1],0,x[2],y[2],z[2],0,');
w('    -(x[0]*eye[0]+x[1]*eye[1]+x[2]*eye[2]),-(y[0]*eye[0]+y[1]*eye[1]+y[2]*eye[2]),');
w('    -(z[0]*eye[0]+z[1]*eye[1]+z[2]*eye[2]),1]);}');
w('');
w('function perspective(fov:number,aspect:number,near:number,far:number):Float32Array{');
w('  const f=1/Math.tan(fov/2),nf=1/(near-far);');
w('  return new Float32Array([f/aspect,0,0,0,0,f,0,0,0,0,far*nf,-1,0,0,far*near*nf,0]);}');
w('');
w('function multiply(a:Float32Array,b:Float32Array):Float32Array{');
w('  const r=new Float32Array(16);');
w('  for(let i=0;i<4;i++)for(let j=0;j<4;j++)');
w('    r[j*4+i]=a[i]*b[j*4]+a[4+i]*b[j*4+1]+a[8+i]*b[j*4+2]+a[12+i]*b[j*4+3];');
w('  return r;}');
w('');
w('class OrbitalCamera{');
w('  theta=Math.PI*0.25;phi=Math.PI*0.35;radius=3.0;target=[0,0,0];');
w('  get eye():[number,number,number]{');
w('    const sp=Math.sin(this.phi),cp=Math.cos(this.phi),st=Math.sin(this.theta),ct=Math.cos(this.theta);');
w('    return[this.target[0]+this.radius*sp*ct,this.target[1]+this.radius*cp,this.target[2]+this.radius*sp*st];}');
w('  orbit(dx:number,dy:number){this.theta-=dx*0.01;this.phi=Math.max(0.1,Math.min(Math.PI-0.1,this.phi-dy*0.01));}');
w('  pan(dx:number,dy:number){const s=this.radius*0.002,st=Math.sin(this.theta),ct=Math.cos(this.theta);');
w('    this.target[0]+=(-st*dx)*s;this.target[1]+=dy*s;this.target[2]+=(ct*dx)*s;}');
w('  zoom(d:number){this.radius=Math.max(0.5,Math.min(50,this.radius*(1+d*0.001)));}');
w('  viewProjMatrix(a:number):Float32Array{return multiply(perspective(Math.PI/4,a,0.01,100),lookAt(this.eye,this.target as any,[0,1,0]));}');
w('}');
w('');
w('function generateGrid(size:number,div:number):{vertices:Float32Array;count:number}{');
w('  const step=size/div,half=size/2,v:number[]=[];');
w('  for(let i=0;i<=div;i++){const p=-half+i*step;if(Math.abs(p)<0.001)continue;const g=0.25;');
w('    v.push(-half,0,p,g,g,g, half,0,p,g,g,g, p,0,-half,g,g,g, p,0,half,g,g,g);}');
w('  return{vertices:new Float32Array(v),count:v.length/6};}');
w('');
w('function generateAxisGizmo(len:number):{vertices:Float32Array;count:number}{');
w('  const v:number[]=[];');
w('  v.push(0,0,0,1,0.2,0.2, len,0,0,1,0.2,0.2);');
w('  v.push(0,0,0,0.2,1,0.2, 0,len,0,0.2,1,0.2);');
w('  v.push(0,0,0,0.3,0.3,1, 0,0,len,0.3,0.3,1);');
w('  return{vertices:new Float32Array(v),count:v.length/6};}');
w('');

// WGSL shaders as string constants
w('const MESH_SHADER=`');
w('struct Uniforms{viewProj:mat4x4f}');
w('@group(0)@binding(0)var<uniform> u:Uniforms;');
w('struct VI{@location(0)p:vec3f,@location(1)n:vec3f,@location(2)c:vec3f}');
w('struct VO{@builtin(position)p:vec4f,@location(0)c:vec3f,@location(1)n:vec3f}');
w('@vertex fn vs(i:VI)->VO{var o:VO;o.p=u.viewProj*vec4f(i.p,1.0);o.c=i.c;o.n=i.n;return o;}');
w('@fragment fn fs(i:VO)->@location(0)vec4f{let ld=normalize(vec3f(0.5,1.0,0.8));let lit=0.3+max(dot(normalize(i.n),ld),0.0)*0.7;return vec4f(i.c*lit,1.0);}`;');
w('');
w('const LINE_SHADER=`');
w('struct Uniforms{viewProj:mat4x4f}');
w('@group(0)@binding(0)var<uniform> u:Uniforms;');
w('struct VI{@location(0)p:vec3f,@location(1)c:vec3f}');
w('struct VO{@builtin(position)p:vec4f,@location(0)c:vec3f}');
w('@vertex fn vs(i:VI)->VO{var o:VO;o.p=u.viewProj*vec4f(i.p,1.0);o.c=i.c;return o;}');
w('@fragment fn fs(i:VO)->@location(0)vec4f{return vec4f(i.c,1.0);}`;');
w('');

w('export class DarkIronRenderer{');
w('  private device:GPUDevice|null=null;private context:GPUCanvasContext|null=null;');
w('  private meshPipeline:GPURenderPipeline|null=null;private linePipeline:GPURenderPipeline|null=null;');
w('  private depthTex:GPUTexture|null=null;private uBuf:GPUBuffer|null=null;');
w('  private meshBG:GPUBindGroup|null=null;private lineBG:GPUBindGroup|null=null;');
w('  private vBuf:GPUBuffer|null=null;private iBuf:GPUBuffer|null=null;private iCount=0;');
w('  private gridBuf:GPUBuffer|null=null;private gridN=0;');
w('  private axisBuf:GPUBuffer|null=null;private axisN=0;');
w('  private cam=new OrbitalCamera();');
w('  constructor(private config:RendererConfig){}');
w('');
w('  async initialize():Promise<boolean>{');
w('    if(!navigator.gpu){console.error("[DarkIron Renderer] WebGPU not supported");return false;}');
w('    const adapter=await navigator.gpu.requestAdapter({powerPreference:"high-performance"});');
w('    if(!adapter)return false;');
w('    this.device=await adapter.requestDevice();');
w('    this.context=this.config.canvas.getContext("webgpu") as GPUCanvasContext;');
w('    const fmt=navigator.gpu.getPreferredCanvasFormat();');
w('    this.context.configure({device:this.device,format:fmt,alphaMode:"premultiplied"});');
w('    this.uBuf=this.device.createBuffer({size:64,usage:GPUBufferUsage.UNIFORM|GPUBufferUsage.COPY_DST});');
w('');
w('    const bgl=this.device.createBindGroupLayout({entries:[{binding:0,visibility:GPUShaderStage.VERTEX,buffer:{type:"uniform"}}]});');
w('    this.meshBG=this.device.createBindGroup({layout:bgl,entries:[{binding:0,resource:{buffer:this.uBuf}}]});');
w('    this.lineBG=this.device.createBindGroup({layout:bgl,entries:[{binding:0,resource:{buffer:this.uBuf}}]});');
w('    const layout=this.device.createPipelineLayout({bindGroupLayouts:[bgl]});');
w('');
w('    const ms=this.device.createShaderModule({code:MESH_SHADER});');
w('    this.meshPipeline=this.device.createRenderPipeline({layout,');
w('      vertex:{module:ms,entryPoint:"vs",buffers:[{arrayStride:36,attributes:[');
w('        {shaderLocation:0,offset:0,format:"float32x3"},{shaderLocation:1,offset:12,format:"float32x3"},{shaderLocation:2,offset:24,format:"float32x3"}]}]},');
w('      fragment:{module:ms,entryPoint:"fs",targets:[{format:fmt}]},');
w('      primitive:{topology:"triangle-list",cullMode:"none"},');
w('      depthStencil:{format:"depth24plus",depthWriteEnabled:true,depthCompare:"less"}});');
w('');
w('    const ls=this.device.createShaderModule({code:LINE_SHADER});');
w('    this.linePipeline=this.device.createRenderPipeline({layout,');
w('      vertex:{module:ls,entryPoint:"vs",buffers:[{arrayStride:24,attributes:[');
w('        {shaderLocation:0,offset:0,format:"float32x3"},{shaderLocation:1,offset:12,format:"float32x3"}]}]},');
w('      fragment:{module:ls,entryPoint:"fs",targets:[{format:fmt}]},');
w('      primitive:{topology:"line-list"},');
w('      depthStencil:{format:"depth24plus",depthWriteEnabled:true,depthCompare:"less"}});');
w('');
w('    const grid=generateGrid(10,20);');
w('    this.gridBuf=this.device.createBuffer({size:grid.vertices.byteLength,usage:GPUBufferUsage.VERTEX|GPUBufferUsage.COPY_DST});');
w('    this.device.queue.writeBuffer(this.gridBuf,0,grid.vertices);this.gridN=grid.count;');
w('    const axis=generateAxisGizmo(1.5);');
w('    this.axisBuf=this.device.createBuffer({size:axis.vertices.byteLength,usage:GPUBufferUsage.VERTEX|GPUBufferUsage.COPY_DST});');
w('    this.device.queue.writeBuffer(this.axisBuf,0,axis.vertices);this.axisN=axis.count;');
w('');
w('    this.depthTex?.destroy();');
w('    this.depthTex=this.device.createTexture({size:[this.config.canvas.width,this.config.canvas.height],format:"depth24plus",usage:GPUTextureUsage.RENDER_ATTACHMENT});');
w('');
w('    const c=this.config.canvas;let drag=false,btn=0,lx=0,ly=0;');
w('    c.addEventListener("mousedown",e=>{drag=true;btn=e.button;lx=e.clientX;ly=e.clientY;e.preventDefault();});');
w('    window.addEventListener("mousemove",e=>{if(!drag)return;const dx=e.clientX-lx,dy=e.clientY-ly;lx=e.clientX;ly=e.clientY;');
w('      if(btn===0)this.cam.orbit(dx,dy);else this.cam.pan(dx,dy);});');
w('    window.addEventListener("mouseup",()=>{drag=false;});');
w('    c.addEventListener("wheel",e=>{this.cam.zoom(e.deltaY);e.preventDefault();},{passive:false});');
w('    c.addEventListener("contextmenu",e=>e.preventDefault());');
w('    console.log("[DarkIron Renderer] Initialized (WebGPU)");return true;}');
w('');
w('  uploadMesh(mesh:MeshData):void{');
w('    if(!this.device)throw new Error("Not init");');
w('    const v=new Float32Array(mesh.vertices),idx=new Uint32Array(mesh.indices);');
w('    this.vBuf?.destroy();this.iBuf?.destroy();');
w('    this.vBuf=this.device.createBuffer({size:v.byteLength,usage:GPUBufferUsage.VERTEX|GPUBufferUsage.COPY_DST});');
w('    this.device.queue.writeBuffer(this.vBuf,0,v);');
w('    this.iBuf=this.device.createBuffer({size:idx.byteLength,usage:GPUBufferUsage.INDEX|GPUBufferUsage.COPY_DST});');
w('    this.device.queue.writeBuffer(this.iBuf,0,idx);this.iCount=idx.length;');
w('    console.log("[DarkIron Renderer] Uploaded mesh: "+mesh.name+" ("+this.iCount+" indices)");}');
w('');
w('  render():void{');
w('    if(!this.device||!this.context||!this.meshPipeline||!this.linePipeline||!this.depthTex||!this.uBuf)return;');
w('    const a=this.config.canvas.width/this.config.canvas.height;');
w('    this.device.queue.writeBuffer(this.uBuf,0,this.cam.viewProjMatrix(a));');
w('    const enc=this.device.createCommandEncoder();');
w('    const pass=enc.beginRenderPass({');
w('      colorAttachments:[{view:this.context.getCurrentTexture().createView(),clearValue:{r:0.08,g:0.08,b:0.10,a:1},loadOp:"clear",storeOp:"store"}],');
w('      depthStencilAttachment:{view:this.depthTex.createView(),depthClearValue:1.0,depthLoadOp:"clear",depthStoreOp:"store"}});');
w('    if(this.gridBuf&&this.lineBG){pass.setPipeline(this.linePipeline);pass.setBindGroup(0,this.lineBG);pass.setVertexBuffer(0,this.gridBuf);pass.draw(this.gridN);}');
w('    if(this.axisBuf&&this.lineBG){pass.setPipeline(this.linePipeline);pass.setBindGroup(0,this.lineBG);pass.setVertexBuffer(0,this.axisBuf);pass.draw(this.axisN);}');
w('    if(this.vBuf&&this.iBuf&&this.meshBG&&this.iCount>0){pass.setPipeline(this.meshPipeline);pass.setBindGroup(0,this.meshBG);');
w('      pass.setVertexBuffer(0,this.vBuf);pass.setIndexBuffer(this.iBuf,"uint32");pass.drawIndexed(this.iCount);}');
w('    pass.end();this.device.queue.submit([enc.finish()]);}');
w('');
w('  destroy():void{this.vBuf?.destroy();this.iBuf?.destroy();this.uBuf?.destroy();');
w('    this.depthTex?.destroy();this.gridBuf?.destroy();this.axisBuf?.destroy();');
w('    this.device?.destroy();console.log("[DarkIron Renderer] Destroyed");}');
w('}');

